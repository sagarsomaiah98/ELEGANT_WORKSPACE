package com.elegant.parameters;

public class Calc {
	
	
	public void Addition(int a, int b)
	{
		
		int c=a+b;
		System.out.println("Addition of two numbers "+a+" and "+b+" is "+c);	
		
	}
	
	public void Subtraction(int t, int l)
	{
		
		int u=t-l;
		System.out.println("Subtraction of two numbers "+t+" and "+l+" is "+u);
		
	}
	public void multiplication(int x, int y) {
		
		
		int z=x*y;
		System.out.println("Multiplication of two numbers "+x+" and "+y +"  is "+z);
	}
	
	public void division(int h, int m) {
		
		int d=h*m;
		System.out.println("Division of two numbers "+h+" and "+m +"  is "+d);
		
	}

}
